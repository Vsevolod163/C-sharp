// Заполнить рисунок двумерный массив

void PrintDvArray(int[,] array)
{
    for(int i = 0; i < array.GetLength(0); i++)
    {
        for(int j = 0; j < array.GetLength(1); j++)
        {
            if(array[i, j] == 1) Console.Write("+");
            else Console.Write(" ");
        }
        Console.WriteLine();
    }
    Console.WriteLine();
}

void FillImage(int row, int col, int[,] array)
{
    if(array[row, col] == 0) 
    {
        array[row, col] = 1;
        FillImage(row - 1, col, array);
        FillImage(row, col - 1, array);
        FillImage(row + 1, col, array);
        FillImage(row, col + 1, array);
    }
}

